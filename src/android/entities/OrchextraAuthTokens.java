package orchextra.entities;

public class OrchextraAuthTokens {

    private String key;
    private String secret;

    public String getKey() {
        return key;
    }

    public String getSecret() {
        return secret;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public void setSecret(String secret) {
        this.secret = secret;
    }
}
